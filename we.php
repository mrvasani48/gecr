        <h2 class="ihead">Overview</h2>
        <ul>
             <li style="list-style-type: none;">In pursuance of the directions issued by the University Grants Commission and Ministry 
                 of Human Resource Development, Government of India,  Government Engineering College Rajkot(GECR)
                  has set up the Women Development Cell (WDC). The Women Development Cell will look after
                   the welfare of the women employees (including teaching, non-teaching and contractual workers)
                    and students, facilitate redressal of their grievances and shall cater to the issues/grievances 
                    concerning women employees. WDC would promote all round development of women employees/students 
                    of the Institute through events, health care awareness, workshops etc. Time to time the cell
                     conducts seminars and lectures by specialists and eminent personalities to
                  stop violence against women, sexual harassment at work and about health, hygiene etc.
            </li>
            </ul>
            <h2 class="ihead">Objectives</h2>
            <ul>
                <li>To resolve issues pertaining to girl’s/women’s sexual harassment</li>
                <li>To resolve issues through Internal Complaints Committee (ICC)</li>
                <li>To equip the female women employees and students with knowledge of their rights</li>
                <li>To create awareness about health and hygiene</li>
                <li>To educate them about gender sensitization</li>
                <li>To ensure personality along with academic development of students   </li>
            </ul>
            <h2 class="ihead">Who can approach the WDC</h2>
            <ul>
             <li style="list-style-type: none;">Any employee including faculty, staff, contractual and student of VGEC can approach the WDC.
            </li>
            </ul>

            <h2 class="ihead">Infrastructure/Facilities</h2>
            <ul>
                <li>A separate ladies room has been given to girl students situated at E-block.</li>
                <li>This room equipped with the necessary infrastructure like first aid box, cupboards, chairs, tables and cots with mattress.</li>
                <li>Suggestion box is kept in the ladies room to receive complaints and suggestions related to the needs of girl students.</li>
                <li>Separate notice board for WDC displays the articles written by girl students and special issues related with girl students.</li>
                <li>One separate room is kept reserved for counselling of girl students.</li>
            </ul>

